/** «Тағы тесттер» қазақ мәтіні тек HTML-де; ru i18n оны ауыстырады. Қазаққа қайтуда осы снимок қалпына келеді. */
const pextraKkSnapshot = {};

function capturePextraKkSnapshotFromDom() {
  document.querySelectorAll('[id^="pextra-"]').forEach((el) => {
    pextraKkSnapshot[el.id] = el.innerHTML;
  });
}

function restorePextraKkFromSnapshot() {
  Object.entries(pextraKkSnapshot).forEach(([id, html]) => {
    const el = document.getElementById(id);
    if (el) {
      el.innerHTML = html;
    }
  });
}

function applyLanguage(lang) {
  const locale = i18n[lang] || i18n.kk;
  document.documentElement.lang = lang;
  document.title = locale.title;

  if (metaDescription) {
    metaDescription.setAttribute("content", locale.description);
  }

  if (menuToggle) {
    menuToggle.setAttribute("aria-label", locale.menuAria);
  }

  if (langToggle) {
    langToggle.textContent = locale.langButton;
  }

  if (lang === "kk") {
    restorePextraKkFromSnapshot();
  }

  Object.entries(locale.text).forEach(([id, value]) => {
    if (id === "perception-img-alt" || id === "about-photo-alt") {
      return;
    }
    if (lang === "kk" && id.startsWith("pextra-")) {
      return;
    }
    const element = document.getElementById(id);
    if (element) {
      element.textContent = value;
    }
  });

  const perceptionImg = document.getElementById("perception-image");
  const perceptionAlt = locale.text["perception-img-alt"];
  if (perceptionImg && perceptionAlt) {
    perceptionImg.alt = perceptionAlt;
  }

  const aboutPhoto = document.getElementById("about-photo");
  const aboutPhotoAlt = locale.text["about-photo-alt"];
  if (aboutPhoto && aboutPhotoAlt) {
    aboutPhoto.alt = aboutPhotoAlt;
  }

  fillPextraPictureBodies(lang);

  if (locale.carouselPrev) {
    document.querySelectorAll(".carousel-prev").forEach((btn) => {
      btn.setAttribute("aria-label", locale.carouselPrev);
    });
  }
  if (locale.carouselNext) {
    document.querySelectorAll(".carousel-next").forEach((btn) => {
      btn.setAttribute("aria-label", locale.carouselNext);
    });
  }

  applyPollLabels(locale);
  syncPollUI();
}

/** Опрос: явная подстановка (после общего цикла), чтобы KK/RU не «залипали» из-за порядка ключей или кэша. */
const POLL_LABEL_IDS = [
  "nav-poll",
  "poll-title",
  "poll-lead",
  "poll-opt-yes",
  "poll-opt-partly",
  "poll-opt-no",
  "poll-submit",
];

function applyPollLabels(locale) {
  const t = locale.text;
  for (const id of POLL_LABEL_IDS) {
    const el = document.getElementById(id);
    const val = t[id];
    if (el && typeof val === "string" && val.length > 0) {
      el.textContent = val;
    }
  }
}

const POLL_STORAGE_KEY = "site-poll-v1";

function pollChoiceLabel(lang, value) {
  const locale = i18n[lang] || i18n.kk;
  const key = { yes: "poll-opt-yes", partly: "poll-opt-partly", no: "poll-opt-no" }[value];
  return key ? locale.text[key] : value;
}

function syncPollUI() {
  const form = document.getElementById("poll-form");
  const statusEl = document.getElementById("poll-status");
  if (!form || !statusEl) {
    return;
  }
  const saved = localStorage.getItem(POLL_STORAGE_KEY);
  const locale = i18n[currentLang] || i18n.kk;
  if (saved && ["yes", "partly", "no"].includes(saved)) {
    form.hidden = true;
    statusEl.hidden = false;
    statusEl.textContent = `${locale.text["poll-your-choice"]} ${pollChoiceLabel(currentLang, saved)}. ${locale.text["poll-thanks"]}`;
  } else {
    form.hidden = false;
    statusEl.hidden = true;
    statusEl.textContent = "";
  }
}

async function initCarousels() {
  const roots = document.querySelectorAll(".photo-carousel");
  for (const root of roots) {
    const key = root.dataset.carousel;
    const listed = carouselData[key];
    if (!listed?.length) {
      continue;
    }

    const img = root.querySelector(".carousel-image");
    const currentEl = root.querySelector(".carousel-current");
    const totalEl = root.querySelector(".carousel-total");
    const prev = root.querySelector(".carousel-prev");
    const next = root.querySelector(".carousel-next");

    if (!img || !currentEl || !totalEl || !prev || !next) {
      continue;
    }

    let existing = await filterExistingImages(listed);
    if (!existing.length) {
      existing = [carouselFallback];
    }

    const images = existing;
    let index = 0;
    let autoplayTimer = null;
    let isInView = false;

    totalEl.textContent = String(images.length);

    function stopAutoplay() {
      if (autoplayTimer !== null) {
        window.clearInterval(autoplayTimer);
        autoplayTimer = null;
      }
    }

    function startAutoplay() {
      if (carouselPrefersReducedMotion || !isInView || images.length < 2) {
        return;
      }
      stopAutoplay();
      autoplayTimer = window.setInterval(() => {
        show(index + 1);
      }, CAROUSEL_AUTOPLAY_MS);
    }

    function show(nextIndex) {
      index = (nextIndex + images.length) % images.length;
      img.onerror = function handleError() {
        img.onerror = null;
        img.src = carouselFallback;
      };
      img.src = images[index];
      currentEl.textContent = String(index + 1);
    }

    function onManualNav(delta) {
      show(index + delta);
      stopAutoplay();
      startAutoplay();
    }

    prev.addEventListener("click", () => {
      onManualNav(-1);
    });
    next.addEventListener("click", () => {
      onManualNav(1);
    });

    root.addEventListener("mouseenter", () => {
      stopAutoplay();
    });
    root.addEventListener("mouseleave", () => {
      startAutoplay();
    });

    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          isInView = entry.isIntersecting && entry.intersectionRatio > 0.05;
          if (isInView) {
            startAutoplay();
          } else {
            stopAutoplay();
          }
        });
      },
      { threshold: [0, 0.1, 0.2] }
    );
    observer.observe(root);

    show(0);
  }
}

