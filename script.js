function fillPextraPictureBodies(lang) {
  const kk = window.PEXTRA_PIC_KK;
  const ru = window.PEXTRA_PIC_RU;
  if (!kk || !ru) {
    return;
  }
  const list = lang === "ru" ? ru : kk;
  for (let i = 1; i <= 6; i += 1) {
    const el = document.getElementById(`pextra-pic-${i}-body`);
    if (el && list[i - 1]) {
      el.innerHTML = list[i - 1];
    }
  }
}

function filterExistingImages(urls) {
  return Promise.all(
    urls.map(
      (url) =>
        new Promise((resolve) => {
          const probe = new Image();
          probe.onload = () => resolve({ url, ok: true });
          probe.onerror = () => resolve({ url, ok: false });
          probe.src = url;
        })
    )
  ).then((results) => results.filter((r) => r.ok).map((r) => r.url));
}

