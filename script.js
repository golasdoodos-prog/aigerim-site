function applyLanguage(lang) {
  const locale = i18n[lang] || i18n.kk;
  document.documentElement.lang = lang;
  document.title = locale.title;

  if (metaDescription) {
    metaDescription.setAttribute("content", locale.description);
  }

  if (menuToggle) {
    menuToggle.setAttribute("aria-label", locale.menuAria);
  }

  if (langToggle) {
    langToggle.textContent = locale.langButton;
  }

  Object.entries(locale.text).forEach(([id, value]) => {
    const element = document.getElementById(id);
    if (element) {
      element.textContent = value;
    }
  });
}

if (menuToggle && menu) {
  menuToggle.addEventListener("click", () => {
    menu.classList.toggle("open");
  });

  menuLinks.forEach((link) => {
    link.addEventListener("click", () => {
      menu.classList.remove("open");
    });
  });
}

tabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    tabs.forEach((btn) => btn.classList.remove("active"));
    tab.classList.add("active");

    const target = tab.dataset.target;
    panels.forEach((panel) => {
      panel.classList.toggle("active", panel.id === target);
    });
  });
});

if (langToggle) {
  langToggle.addEventListener("click", () => {
    currentLang = currentLang === "kk" ? "ru" : "kk";
    localStorage.setItem("site-lang", currentLang);
    applyLanguage(currentLang);
  });
}

if (feedbackForm && status) {
  feedbackForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const name = feedbackForm.name.value.trim();
    const message = feedbackForm.message.value.trim();
    const locale = i18n[currentLang] || i18n.kk;

    if (!name || !message) {
      status.textContent = locale.required;
      return;
    }

    status.textContent = locale.success;
    feedbackForm.reset();
  });
}

applyLanguage(currentLang);
